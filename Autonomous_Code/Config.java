package org.firstinspires.ftc.teamcode;

public class Config {
  static double SPEED;
  static double CORRECTION;
  static double FORWARD;
  static double TURN;
  public Config(double speed, double correction, double forward, double turn) {
    this.SPEED = speed;
    this.CORRECTION = correction;
    this.FORWARD = forward;
    this.TURN = turn;
  }
}
